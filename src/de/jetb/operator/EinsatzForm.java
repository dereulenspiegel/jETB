    private void joinButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_joinButtonActionPerformed
        String einsatzNummer = null;
        if(einsatznummerField.getText()!=null &&
                einsatznummerField.getText().trim().length()>0){
            einsatzNummer = einsatznummerField.getText().trim();
        } else {
            einsatzNummer = (String)einsatzListe.getSelectedValue();
        }

        if(einsatzNummer!=null){
            LLPresence myPresence = runner.getService().getLocalPresence();
            myPresence.setMsg(einsatzNummer);
            try {
                runner.getService().updatePresence(myPresence);
                MainWindow window = new MainWindow(runner);
                window.setVisible(true);
                this.setVisible(false);
                this.dispose();
            } catch (XMPPException ex) {
                //TODO: Handle Exception
                ex.printStackTrace();
            }
        } else {
            //TODO: Notify the user that he has to enter a number
        }
    }//GEN-LAST:event_joinButtonActionPerformed


