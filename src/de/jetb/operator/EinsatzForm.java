    private class EinsatzListModel extends AbstractListModel implements PresenceListener{

        private ArrayList<String> einsatzNummern = new ArrayList<String>();

        public void addEinsatzNummer(String nummer){
            if(nummer!=null && !einsatzNummern.contains(nummer)){
                einsatzNummern.add(nummer);
                this.fireContentsChanged(this, 0, einsatzNummern.size());
            }
        }

        public void removeEinsatzNummer(String nummer){
            if(nummer!=null && einsatzNummern.contains(nummer)){
                einsatzNummern.remove(nummer);
                this.fireContentsChanged(this, 0, einsatzNummern.size());
            }
        }

        public int getSize() {
            return einsatzNummern.size();
        }

        public Object getElementAt(int index) {
            return einsatzNummern.get(index);
        }

        public void presenceNew(LLPresence presence) {
            addEinsatzNummer(presence.getMsg());
        }

        public void presenceRemoved(LLPresence presence) {
            removeEinsatzNummer(presence.getMsg());
        }

    }
